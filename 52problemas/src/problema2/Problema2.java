public class Problema2 {
/**
 * 
 * @param args 
 * Hacer un pseudocodigo que imprima los numeros del 100 al 0, 
 * en orden decreciente.
 */
    public static void main(String[] args) {
        for (int i = 100; i >= 0; i--) {
            System.out.println(i);
        }
    }
}
